function addNum(num1,num2) {
    let result = num1 + num2;
    return result;
}


function subtractNum(num1,num2) {
    let result = num1 - num2;
    return result;
}

function multiplyNum(num1, num2) {
    let result = num1 * num2;
    return result;
}


function divideNum(num1,num2) {
    let result = num1 / num2;
    return result;
}




